<div id="x-rays">
	<h2>X-rays</h2>
		<p>At Foster Family Dental, we take every precaution to reduce radiation to a level that is as low as reasonable acceptable.  Using double-leaded protective vests with circumferential thyroid collars and digital x-ray sensors, we have drastically reduced patients exposure to radiation in comparison to years past.</p>

		<p>Dental x-rays in and of themselves are not a significant source of radiation.  In fact, enjoying an hour of beautiful Colorado sunshine exposes you to more radiation than your check-up radiographs.   Dental professionals can only see 60% of your tooth structure and 0% of the supporting bone without x-rays.  The dental and periodontal problems associated with NOT getting recommended x-rays are far more risky than the negligible amount of radiation to which you are exposed.</p> 

		<p>If you are at minimal risk to dental decay and gum disease, Dr. Foster is happy to recommend an extended x-ray schedule as an alternative to the annual schedule recommended by the American Dental Association.</p>  
