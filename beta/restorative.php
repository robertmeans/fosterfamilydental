<div id="crowns" class="services-ul">
	<h2>Crowns</h2>
	<p>When a tooth presents in a condition beyond a simple composite filling repair, crowns are an excellent choice to restore it to its proper shape and function.  There are many different crowns, and each crown has its advantages depending on the ultimate function of the tooth.  If crowns are necessary for you, Dr. Foster will discuss each different type and make a recommendation.</p>
	<ul>
		<li><span class="ul-heading">Full Gold Crowns</span> - These crowns look gold, but are composed of a mixture of metals with a high gold content.  Gold crowns are excellent for back teeth (molars) and have a record for being one of the longest lasting types of crowns.</li>
		<li><span class="ul-heading">All Porcelain Crowns</span>-  These metal-free crowns are composed solely of porcelain, however there are many different types of porcelain crowns, some of which are more esthetic than others.  Dr. Foster utilizes a local Colorado lab known for their esthetic ceramic restorations and together we can transform your smile with high-quality, long-lasting materials.</li>
	</ul>
	<p>When is a crown necessary?</p>
	<ul>
		<li>When a tooth is cracked or brittle</li>
		<li>After root canal therapy</li>
		<li>When a tooth has a large amount of decay that a filling cannot restore adequately</li>
		<li>When large, old fillings are breaking down or cracking the remaining tooth structure</li>
		<li>For esthetic purposes, if you have a desire to improve your smile</li>
	</ul>
