	<h2>Bridges</h2>
		<p>Bridges, like crowns and other restorations, should not be considered a permanent solution.  Though materials continue to improve, these restorations are susceptible to breaking, decay, wear, etc.  However, with proper maintenance, many patients utilize these restorations for many, many years.</p>
