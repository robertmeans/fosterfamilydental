<div id="dentures-partials">
	<h2>Dentures &amp; Partials</h2>
		<p>When many or all of your teeth are missing, dentures and partials are an effective way to restore your smile, both esthetically and functionally.</p>  

		<p>Complete dentures are used when all of the teeth are missing.  Should complete dentures be in your future, Dr. Foster can discuss your options to make sure you do not have to go ANY time without teeth.  Complete dentures take time to make correctly and take time to get adjusted to.  Experiences differ from patient to patient; should you need more support, implants can be placed so your dentures can “snap” into place.  Though a longer process and more expensive, patients are usually much happier with implant-supported dentures.</p>  

		<p>Partials can replace as little or as many missing teeth a patient may have.  There are many types of partials some made with metal with acrylic and others made fully of acrylic or nylon.  Dr. Foster is happy to go over your options and the benefits of each type of partial should they be a treatment option for you.</p>

