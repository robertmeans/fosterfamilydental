<div id="composite-fillings">
	<h2>Composite (tooth-colored) Fillings</h2>
		<p>Composite fillings are an aesthetic and conservative way to treat tooth decay. Composite fillings are ideal for replacing small to moderate amounts of tooth structure. These types of fillings are adhesively bonded to teeth, allowing you to preserve more of your natural tooth. With composite fillings, we can closely match the shade of your natural tooth.  Composite fillings can also replace discolored and leaking metal fillings, repair chipped teeth, and close spaces between front teeth.</p>

		<p>At Foster Family Dental, we have chosen to provide our patients with a mercury-free facility and do not place amalgam (silver) fillings.  When placed correctly, new generations of composite filling material can last a long time.  Composite fillings require much less tooth structure to be removed and sometimes can be placed without being numb.  Additionally, you can eat on tooth-colored fillings immediately after they are placed.  There are many benefits in restoring your tooth with composite fillings.</p> 

