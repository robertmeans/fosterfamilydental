		<h2>Root Canal Treatment</h2>
		<p>When the inner nerve of a tooth becomes infected or traumatized by trauma or tooth decay, it begins to die.  This process can be painful or can proceed undetected, with little to no pain at all.  Regardless, once the nerve has died, it becomes necrotic, and the tooth harbors bacteria that cause a chronic infection that cannot be remedied by antibiotics alone.</p>  

		<p>Root canals themselves have a negative reputation.   However, root canal procedures are typically not painful, can be performed relatively quickly, and actually make a tooth feel MUCH better once completed.</p>

		<p>A tooth may need a root canal if:</p>

		<ul>
			<li>You have a swelling or abscess on your gums</li>
			<li>It has become very hot or cold sensitive</li>
			<li>It hurts to bite</li>
			<li>An x-ray detects an abscess</li>
			<li>It hurts constantly or wakes you up in the middle of the night</li>
		</ul>

		<p>Dr. Foster performs most root canals in her office, and many times can complete the procedure in one appointment.  If you would be better served seeing a root canal specialist, Dr. Foster works closely with the top specialist in Denver and can get you seen right away.</p>

		<p>If you are having pain, or any of the symptoms listed above, Dr. Foster is available to see you, as emergencies are seen the same day.  Please call the office, and if it after business hours, you will be directed to her cell phone.</p>
